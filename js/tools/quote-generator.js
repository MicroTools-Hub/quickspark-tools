let currentCategory = 'Inspiration';

function generateQuote() {
    const category = document.getElementById('quoteCategory')?.value || 'Inspiration';
    const categoryQuotes = quoteCategories[category];
    if (!categoryQuotes) return;
    
    currentCategory = category;
    const quote = categoryQuotes[Math.floor(Math.random() * categoryQuotes.length)];
    
    document.getElementById('quoteText').textContent = `"${quote.text}"`;
    document.getElementById('quoteAuthor').textContent = `— ${quote.author}`;
    document.getElementById('quoteCategory').value = category;
}

function copyQuote() {
    const text = document.getElementById('quoteText').textContent;
    const author = document.getElementById('quoteAuthor').textContent;
    if (text) {
        copyToClipboard(`${text} ${author}`);
    }
}

function shareQuote() {
    const text = document.getElementById('quoteText').textContent;
    const author = document.getElementById('quoteAuthor').textContent;
    const shareText = `${text} ${author}`;
    
    if (navigator.share) {
        navigator.share({
            title: 'Inspirational Quote',
            text: shareText
        });
    } else {
        copyToClipboard(shareText);
        showNotification('Quote copied to clipboard!');
    }
}

function tweetQuote() {
    const text = document.getElementById('quoteText').textContent;
    const author = document.getElementById('quoteAuthor').textContent;
    const shareText = encodeURIComponent(`${text} ${author}`);
    window.open(`https://twitter.com/intent/tweet?text=${shareText}`, '_blank');
}

function updateCategoryInfo() {
    const category = document.getElementById('quoteCategory').value;
    const count = quoteCategories[category]?.length || 0;
    const countEl = document.getElementById('categoryQuoteCount');
    if (countEl) {
        countEl.textContent = `${count} quotes in this category`;
    }
}

function initQuoteGen() {
    // Populate category selector
    const select = document.getElementById('quoteCategory');
    if (select) {
        select.innerHTML = '';
        Object.keys(quoteCategories).forEach(category => {
            const option = document.createElement('option');
            option.value = category;
            option.textContent = category;
            select.appendChild(option);
        });
        select.value = 'Inspiration';
        select.addEventListener('change', () => {
            updateCategoryInfo();
            generateQuote();
        });
    }
    
    updateCategoryInfo();
    setTimeout(generateQuote, 100);
}

// Public API
window.generateQuote = generateQuote;
window.copyQuote = copyQuote;
window.shareQuote = shareQuote;
window.tweetQuote = tweetQuote;

// Initialize
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', initQuoteGen);
} else {
    initQuoteGen();
}
